class Pessoa {
  final String id;
  final int tipoPessoa;
  final String cpf;
  final String nome;
  final String cep;
  final String endereco;
  final String complemento;
  final String bairro;
  final String municipio;
  final String uf;
  final double valor;
  final int isento;

  Pessoa({
    required this.id,
    required this.tipoPessoa,
    required this.cpf,
    required this.nome,
    required this.cep,
    required this.endereco,
    required this.complemento,
    required this.bairro,
    required this.municipio,
    required this.uf,
    required this.valor,
    required this.isento,
  });
}
